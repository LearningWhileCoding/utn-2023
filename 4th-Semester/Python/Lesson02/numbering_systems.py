# Decimal
a = 10
print(f'a Decimal: {a}')

# Binary
a = 0b1010
print(f'a Binary: {a}')

# Octal
a = 0o12
print(f'a Octal: {a}')

# Hexadecimal
a = 0xA
print(f'a Hexadecimal: {a}')

# Decimal Base
a = int('23', 10)
print(f"a = decimal base : {a}")

# Binary Base
a = int('1010', 2)
print(f"a = binary base : {a}")

# Octal Base
a = int('12', 8)
print(f"a octal base : {a}")

# Hexadecimal Base
a = int('A',16)
print(f"a hexadecimal base: {a}")

# Base 5 values from 0 to 4
a = int ('344',5)
print(f" a = Base 5: {a}")